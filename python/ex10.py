# Ex10 ESCAPE SEQUENCES

# Few escape sequences in python

# declare some variables with strings for values
tabby_cat = "\tI'm tabbed in." # printed line is tabbed '\t'
persian_cat = "I'm split\non a line." # string is devided into two lines '\n'
backslash_cat = "I'm \\ a \\ cat." # '\' is printed in a string by using '\\'

fat_cat = """
I'll do a list:
\t* Cat Food
\t* Fishies
\t* Catnip\n\t* Grass """ #  grass is printed on a new line and tabbed as well

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(print(fat_cat))
