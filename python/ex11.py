# Ex11 ASKING QUESTIONS

# Simple program asking the user for input

print("How old are You?", end = ' ') # printing a string and cancelling out '\n' (newline) at the end
age = input() # asking for input and storing it in a variable
print("How tall are You?", end = ' ')
height = input()
print("How much do You weigh?", end = ' ')
weight = input()

print(f"So, You are {age} years old, {height} tall and {weight} heavy.")
